    public String hashMyPassword () {
      String password = String.valueOf(passwordRegister.getPassword());
      
     String passLength = String.valueOf(password.length());
     Argon2Function myArgon2 = Argon2Function.getInstance(1024, 3, 2, 32, Argon2.D, 19);

     Hash hash = Password.hash(password).addSalt(passLength).with(myArgon2);
     
     return Base64.getEncoder().encodeToString(hash.getBytes());
    }

