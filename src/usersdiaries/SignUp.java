    public boolean checkFieldsValidity() {
        String fullName = fullNameRegister.getText();
        String userName = userNameRegister.getText();
        String password = String.valueOf(passwordRegister.getPassword());
        String passwordConfirmation = String.valueOf(confirmPassRegister.getPassword());
        
        if( fullName.trim().equals("") ||
            userName.trim().equals("") ||
            password.trim().equals("") ||
            passwordConfirmation.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "One Or More Fields Are Empty", "Empty Fields", 2);
            return false;
        } else if (!password.equals(passwordConfirmation)) {
            JOptionPane.showMessageDialog(null, "Passwords doesn't match", "Confirm Password", 2);
            return false;
        } else {
            return true;
        }         
    }
    
    public boolean checkIfUsernameExists(String username) {
        PreparedStatement st;
        ResultSet rs;
        
        boolean username_exists = false;
        
        String query = "SELECT * FROM `users` WHERE `username` = ? ";
        
        
        try {
            st = dbConnection.getConnection().prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();
            
            if(rs.next()) {
                username_exists = true;
                JOptionPane.showMessageDialog(null, "You must introduce another username. This is already taken","Username failed",2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
        }
        return username_exists;
    }
